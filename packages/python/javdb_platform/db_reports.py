"""Report session management for JAVDB AutoSpider.

Handles ReportSessions, ReportMovies, and ReportTorrents tables in reports.db.

ReportSessions tracks each pipeline run with metadata (report type, date, URL,
status, run identity). Each session can be in one of these states:
- 'in_progress' — Session is active
- 'finalizing' — Pending writes are being committed
- 'committed' — Session completed successfully
- 'failed' — Session failed (marked before rollback)

ReportMovies and ReportTorrents store the snapshot of movies and torrents
discovered in each run (used for CSV report generation).
"""

import os
import time
from datetime import datetime
import sqlite3
from typing import Dict, List, Optional, Tuple

from packages.python.javdb_platform.config_helper import cfg
from packages.python.javdb_platform.logging_config import get_logger

logger = get_logger(__name__)

# Lazy imports to avoid circular dependencies
_get_db = None
_get_local_sqlite_db = None
_REPORTS_DB_PATH = None
_HISTORY_DB_PATH = None
_generate_session_id = None
_resolve_write_mode = None
_DB_OPERATIONAL_ERRORS = None


def _ensure_imports():
    """Lazy import to avoid circular dependency with db_connection and db_session."""
    global _get_db, _get_local_sqlite_db, _REPORTS_DB_PATH, _HISTORY_DB_PATH
    global _generate_session_id, _resolve_write_mode, _DB_OPERATIONAL_ERRORS
    if _get_db is None:
        from packages.python.javdb_platform.db_connection import (
            get_db,
            get_local_sqlite_db,
            REPORTS_DB_PATH,
            HISTORY_DB_PATH,
        )
        from packages.python.javdb_platform.db_session import (
            generate_session_id,
            _resolve_write_mode as resolve_wm,
        )
        _get_db = get_db
        _get_local_sqlite_db = get_local_sqlite_db
        _REPORTS_DB_PATH = REPORTS_DB_PATH
        _HISTORY_DB_PATH = HISTORY_DB_PATH
        _generate_session_id = generate_session_id
        _resolve_write_mode = resolve_wm

        try:
            from packages.python.javdb_platform.d1_client import D1PermanentError
            _DB_OPERATIONAL_ERRORS = (sqlite3.OperationalError, D1PermanentError)
        except ImportError:
            _DB_OPERATIONAL_ERRORS = (sqlite3.OperationalError,)


# ── Session creation ─────────────────────────────────────────────────────


def db_create_report_session(
    report_type: str,
    report_date: str,
    csv_filename: str,
    *,
    url_type: Optional[str] = None,
    display_name: Optional[str] = None,
    url: Optional[str] = None,
    start_page: Optional[int] = None,
    end_page: Optional[int] = None,
    created_at: Optional[str] = None,
    db_path: Optional[str] = None,
    run_id: Optional[str] = None,
    run_attempt: Optional[int] = None,
    session_id: Optional[str] = None,
    write_mode: Optional[str] = None,
) -> str:
    """Create a new report session and return its id.

    The Id is generated by the application (via generate_session_id()) and
    inserted explicitly, so SQLite and Cloudflare D1 see the same value.

    Args:
        report_type: Type of report ('daily', 'adhoc', etc.)
        report_date: Report date (YYYY-MM-DD)
        csv_filename: CSV filename for this session
        url_type: Optional URL type
        display_name: Optional display name
        url: Optional source URL
        start_page: Optional start page number
        end_page: Optional end page number
        created_at: Optional creation timestamp (defaults to now)
        db_path: Database path (defaults to REPORTS_DB_PATH)
        run_id: GitHub Actions run_id (optional)
        run_attempt: GitHub Actions run_attempt (optional)
        session_id: Optional explicit session ID (for migration tool)
        write_mode: 'audit' or 'pending' (defaults to env var or 'audit')

    Returns:
        Session ID (TEXT format)

    Raises:
        RuntimeError: If JAVDB_FORBID_DB_WRITES=1 is set
    """
    _ensure_imports()

    from packages.python.javdb_platform.config_helper import db_writes_forbidden
    if db_writes_forbidden():
        raise RuntimeError(
            "db_create_report_session refused: "
            "JAVDB_FORBID_DB_WRITES=1 is engaged. This kill switch is "
            "set by smoke-test workflows (TestIngestion) that must not "
            "pollute production D1 / SQLite. Unset it if you really "
            "need to write to a database."
        )

    if created_at is None:
        created_at = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    sid = session_id if session_id is not None else _generate_session_id()
    resolved_mode = _resolve_write_mode(write_mode)

    with _get_db(db_path or _REPORTS_DB_PATH) as conn:
        conn.execute(
            """INSERT INTO ReportSessions
               (Id, ReportType, ReportDate, UrlType, DisplayName,
                Url, StartPage, EndPage, CsvFilename, DateTimeCreated,
                Status, RunId, RunAttempt, WriteMode)
               VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 'in_progress', ?, ?, ?)""",
            (sid, report_type, report_date, url_type, display_name,
             url, start_page, end_page, csv_filename, created_at,
             run_id, run_attempt, resolved_mode),
        )
    return sid


# ── Session status updates ───────────────────────────────────────────────


def db_mark_session_committed(
    session_id: str,
    db_path: Optional[str] = None,
) -> int:
    """Mark a session as 'committed' so it survives any future cleanup.

    Once a session is committed, its audit rows in MovieHistoryAudit /
    TorrentHistoryAudit are pruned (the rollback CLI refuses committed
    sessions anyway, so the audit log is no longer needed).

    Args:
        session_id: Session identifier
        db_path: Database path (defaults to REPORTS_DB_PATH)

    Returns:
        Number of ReportSessions rows updated (0 if not found or already committed)
    """
    _ensure_imports()

    with _get_db(db_path or _REPORTS_DB_PATH) as conn:
        cur = conn.execute(
            "UPDATE ReportSessions SET Status='committed' WHERE Id=? "
            "AND Status IS NOT 'committed'",
            (session_id,),
        )
        marked = cur.rowcount or 0

    # Audit retention: prune audit rows for this session even when the
    # ReportSessions update was a no-op (Status was already 'committed' on
    # a previous call). Keeps the tables bounded if commit is retried.
    try:
        with _get_db(_HISTORY_DB_PATH) as conn:
            mh = conn.execute(
                "DELETE FROM MovieHistoryAudit WHERE SessionId=?",
                (session_id,),
            )
            th = conn.execute(
                "DELETE FROM TorrentHistoryAudit WHERE SessionId=?",
                (session_id,),
            )
        pruned = (mh.rowcount or 0) + (th.rowcount or 0)
        if pruned > 0:
            logger.info(
                "Pruned %d audit row(s) on commit of session %s",
                pruned, session_id,
            )
    except Exception as exc:  # noqa: BLE001 - best-effort retention
        logger.warning(
            "Could not prune audit rows for committed session %s: %s",
            session_id, exc,
        )

    return marked


def db_mark_session_failed(
    session_id: str,
    db_path: Optional[str] = None,
    *,
    reason: Optional[str] = None,
) -> int:
    """Mark a session as 'failed' (debug-only flag set right before delete).

    Args:
        session_id: Session identifier
        db_path: Database path (defaults to REPORTS_DB_PATH)
        reason: Optional failure reason (e.g., 'workflow_cancel', 'stale_timeout')

    Returns:
        Number of rows updated
    """
    _ensure_imports()

    with _get_db(db_path or _REPORTS_DB_PATH) as conn:
        cur = conn.execute(
            "UPDATE ReportSessions SET Status='failed', FailureReason=? "
            "WHERE Id=?",
            (reason, session_id),
        )
        return cur.rowcount or 0


