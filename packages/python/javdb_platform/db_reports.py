def db_get_session_status(
    session_id: str,
    *,
    db_path: Optional[str] = None,
) -> Optional[Tuple[str, str]]:
    """Return (WriteMode, Status) for session_id, or None if absent.

    Args:
        session_id: Session identifier
        db_path: Database path (defaults to REPORTS_DB_PATH)

    Returns:
        Tuple of (write_mode, status) or None if session not found
    """
    _ensure_imports()

    with _get_db(db_path or _REPORTS_DB_PATH) as conn:
        try:
            row = conn.execute(
                "SELECT COALESCE(WriteMode,'audit') AS WriteMode, Status "
                "FROM ReportSessions WHERE Id=?",
                (session_id,),
            ).fetchone()
        except _DB_OPERATIONAL_ERRORS:
            # Legacy schema without WriteMode column
            row = conn.execute(
                "SELECT Status FROM ReportSessions WHERE Id=?",
                (session_id,),
            ).fetchone()
            if row:
                return ("audit", row["Status"])
            return None
    if row:
        return (row["WriteMode"], row["Status"])
    return None


def db_find_in_progress_sessions(
    *,
    since: Optional[str] = None,
    db_path: Optional[str] = None,
    max_age_hours: Optional[float] = None,
    require_run_identity: bool = False,
) -> List[str]:
    """Return ReportSessions.Id rows still flagged 'in_progress'.

    Args:
        since: ISO timestamp to restrict search (sessions created >= since)
        db_path: Database path (defaults to REPORTS_DB_PATH)
        max_age_hours: Alternative window (sessions older than now - max_age_hours)
        require_run_identity: Only include sessions with non-empty RunId

    Returns:
        List of session IDs

    Raises:
        ValueError: If both since and max_age_hours are provided
    """
    _ensure_imports()

    with _get_db(db_path or _REPORTS_DB_PATH) as conn:
        if since and max_age_hours is not None:
            raise ValueError(
                "db_find_in_progress_sessions: pass either 'since' or "
                "'max_age_hours', not both"
            )
        run_identity_clause = (
            " AND RunId IS NOT NULL AND TRIM(RunId) != ''"
            if require_run_identity else ""
        )

        if max_age_hours is not None:
            cutoff_ts = time.time() - (max_age_hours * 3600)
            cutoff = datetime.utcfromtimestamp(cutoff_ts).strftime(
                "%Y-%m-%d %H:%M:%S"
            )
            rows = conn.execute(
                "SELECT Id FROM ReportSessions "
                "WHERE Status='in_progress' AND DateTimeCreated < ?"
                + run_identity_clause,
                (cutoff,),
            ).fetchall()
        elif since:
            rows = conn.execute(
                "SELECT Id FROM ReportSessions "
                "WHERE Status='in_progress' AND DateTimeCreated >= ?"
                + run_identity_clause,
                (since,),
            ).fetchall()
        else:
            rows = conn.execute(
                "SELECT Id FROM ReportSessions WHERE Status='in_progress'"
                + run_identity_clause,
            ).fetchall()
    return [r['Id'] for r in rows]


def db_find_stale_pending_sessions(
    *,
    db_path: Optional[str] = None,
    max_age_hours: float = 48.0,
    require_run_identity: bool = True,
) -> List[Tuple[str, str, str]]:
    """Return [(Id, Status, WriteMode), ...] for stale Phase 3 sessions.

    Used by cleanup_stale_in_progress to dispatch in_progress sessions to
    rollback and finalizing sessions to resume_commit.

    Args:
        db_path: Database path (defaults to REPORTS_DB_PATH)
        max_age_hours: Age threshold in hours (default 48)
        require_run_identity: Skip legacy rows with empty RunId

    Returns:
        List of (session_id, status, write_mode) tuples
    """
    _ensure_imports()

    cutoff_ts = time.time() - (max_age_hours * 3600)
    cutoff = datetime.utcfromtimestamp(cutoff_ts).strftime(
        "%Y-%m-%d %H:%M:%S",
    )
    run_identity_clause = (
        " AND RunId IS NOT NULL AND TRIM(RunId) != ''"
        if require_run_identity else ""
    )

    with _get_db(db_path or _REPORTS_DB_PATH) as conn:
        try:
            rows = conn.execute(
                "SELECT Id, Status, COALESCE(WriteMode,'audit') AS WriteMode "
                "FROM ReportSessions "
                "WHERE Status IN ('in_progress','finalizing') "
                "AND DateTimeCreated < ?" + run_identity_clause,
                (cutoff,),
            ).fetchall()
        except _DB_OPERATIONAL_ERRORS:
            # Legacy schema without WriteMode column
            rows = conn.execute(
                "SELECT Id, Status FROM ReportSessions "
                "WHERE Status IN ('in_progress','finalizing') "
                "AND DateTimeCreated < ?" + run_identity_clause,
                (cutoff,),
            ).fetchall()
            return [(r["Id"], r["Status"], "audit") for r in rows]
    return [
        (r["Id"], r["Status"], r["WriteMode"]) for r in rows
    ]


def db_count_in_progress_sessions_for_run(
    run_id: str,
    run_attempt: Optional[int] = None,
    *,
    db_path: Optional[str] = None,
) -> int:
    """Count 'in_progress' sessions belonging to a (RunId, RunAttempt) pair.

    Used by the spider self-check: a single workflow run must own at most
    one in-progress session at any given time.

    Args:
        run_id: GitHub Actions run_id
        run_attempt: GitHub Actions run_attempt (optional)
        db_path: Database path (defaults to REPORTS_DB_PATH)

    Returns:
        Count of in-progress sessions
    """
    _ensure_imports()

    with _get_db(db_path or _REPORTS_DB_PATH) as conn:
        if run_attempt is None:
            row = conn.execute(
                "SELECT COUNT(*) AS n FROM ReportSessions "
                "WHERE Status='in_progress' AND RunId=?",
                (run_id,),
            ).fetchone()
        else:
            row = conn.execute(
                "SELECT COUNT(*) AS n FROM ReportSessions "
                "WHERE Status='in_progress' AND RunId=? AND RunAttempt=?",
                (run_id, int(run_attempt)),
            ).fetchone()
    return int((row or {'n': 0})['n'] or 0)


# ── ReportMovies / ReportTorrents ────────────────────────────────────────


def db_insert_report_rows(
    session_id: str,
    rows: List[dict],
    db_path: Optional[str] = None,
) -> int:
    """Insert report rows into ReportMovies + ReportTorrents.

    Accepts rows in the legacy flat dict format with keys like
    hacked_subtitle, subtitle, etc. Each non-empty magnet
    becomes a separate ReportTorrents row.

    Args:
        session_id: Session identifier
        rows: List of movie dicts with torrent data
        db_path: Database path (defaults to REPORTS_DB_PATH)

    Returns:
        Number of movies inserted
    """
    _ensure_imports()

    from apps.api.parsers.common import javdb_absolute_url

    _CATS = [
        ('hacked_subtitle',    'size_hacked_subtitle',    'file_count_hacked_subtitle',    'resolution_hacked_subtitle',    1, 0),
        ('hacked_no_subtitle', 'size_hacked_no_subtitle', 'file_count_hacked_no_subtitle', 'resolution_hacked_no_subtitle', 0, 0),
        ('subtitle',           'size_subtitle',           'file_count_subtitle',           'resolution_subtitle',           1, 1),
        ('no_subtitle',        'size_no_subtitle',        'file_count_no_subtitle',        'resolution_no_subtitle',        0, 1),
    ]
    base_url = cfg('BASE_URL', 'https://javdb.com')

    with _get_db(db_path or _REPORTS_DB_PATH) as conn:
        for row in rows:
            href = javdb_absolute_url(row.get('href') or '', base_url)
            cur = conn.execute(
                """INSERT INTO ReportMovies
                   (SessionId, Href, VideoCode, Page, Actor, Rate, CommentNumber)
                   VALUES (?, ?, ?, ?, ?, ?, ?)""",
                (session_id,
                 href, row.get('video_code'),
                 int(row['page']) if row.get('page') else None,
                 row.get('actor'),
                 float(row['rate']) if row.get('rate') else None,
                 int(row['comment_number']) if row.get('comment_number') else None),
            )
            rm_id = cur.lastrowid
            vc = row.get('video_code')
            for cat, size_cat, fc_cat, res_cat, sub_ind, cen_ind in _CATS:
                magnet = (row.get(cat) or '').strip()
                if magnet:
                    conn.execute(
                        """INSERT INTO ReportTorrents
                           (ReportMovieId, VideoCode, MagnetUri,
                            SubtitleIndicator, CensorIndicator,
                            ResolutionType, Size, FileCount)
                           VALUES (?, ?, ?, ?, ?, ?, ?, ?)""",
                        (rm_id, vc, magnet, sub_ind, cen_ind,
                         row.get(res_cat),
                         row.get(size_cat),
                         int(row.get(fc_cat, 0) or 0)),
                    )
        return len(rows)


def db_get_report_rows(
    session_id: str,
    db_path: Optional[str] = None,
) -> List[dict]:
    """Get all rows for a session as flat dicts (backward compatible).

    Aggregates ReportMovies + ReportTorrents back into the legacy format
    with hacked_subtitle, subtitle, etc. columns.

    Args:
        session_id: Session identifier
        db_path: Database path (defaults to REPORTS_DB_PATH)

    Returns:
        List of movie dicts with aggregated torrent data
    """
    _ensure_imports()

    def indicators_to_category(sub_ind: int, cen_ind: int) -> str:
        """Map (SubtitleIndicator, CensorIndicator) to category name."""
        if sub_ind == 1 and cen_ind == 0:
            return 'hacked_subtitle'
        elif sub_ind == 0 and cen_ind == 0:
            return 'hacked_no_subtitle'
        elif sub_ind == 1 and cen_ind == 1:
            return 'subtitle'
        else:  # sub_ind == 0 and cen_ind == 1
            return 'no_subtitle'

    with _get_db(db_path or _REPORTS_DB_PATH) as conn:
        movies = conn.execute(
            "SELECT * FROM ReportMovies WHERE SessionId = ? ORDER BY Id",
            (session_id,),
        ).fetchall()

        result = []
        for m in movies:
            m = dict(m)
            flat = {
                'href': m.get('Href', ''),
                'video_code': m.get('VideoCode', ''),
                'page': m.get('Page'),
                'actor': m.get('Actor', ''),
                'rate': m.get('Rate'),
                'comment_number': m.get('CommentNumber'),
                'hacked_subtitle': '',
                'hacked_no_subtitle': '',
                'subtitle': '',
                'no_subtitle': '',
                'size_hacked_subtitle': '',
                'size_hacked_no_subtitle': '',
                'size_subtitle': '',
                'size_no_subtitle': '',
                'file_count_hacked_subtitle': 0,
                'file_count_hacked_no_subtitle': 0,
                'file_count_subtitle': 0,
                'file_count_no_subtitle': 0,
                'resolution_hacked_subtitle': None,
                'resolution_hacked_no_subtitle': None,
                'resolution_subtitle': None,
                'resolution_no_subtitle': None,
            }
            torrents = conn.execute(
                "SELECT * FROM ReportTorrents WHERE ReportMovieId = ?",
                (m['Id'],),
            ).fetchall()
            for t in torrents:
                t = dict(t)
                cat = indicators_to_category(t['SubtitleIndicator'], t['CensorIndicator'])
                flat[cat] = t.get('MagnetUri', '')
                flat[f'size_{cat}'] = t.get('Size', '')
                flat[f'file_count_{cat}'] = t.get('FileCount', 0)
                flat[f'resolution_{cat}'] = t.get('ResolutionType')
            result.append(flat)
        return result


def db_get_latest_session(
    report_type: Optional[str] = None,
    db_path: Optional[str] = None,
) -> Optional[dict]:
    """Get the latest session (by Id DESC).

    Args:
        report_type: Optional filter by report type
        db_path: Database path (defaults to REPORTS_DB_PATH)

    Returns:
        Session dict or None if no sessions found
    """
    _ensure_imports()

    with _get_db(db_path or _REPORTS_DB_PATH) as conn:
        if report_type is not None:
            row = conn.execute(
                "SELECT * FROM ReportSessions WHERE ReportType = ? "
                "ORDER BY Id DESC LIMIT 1",
                (report_type,),
            ).fetchone()
        else:
            row = conn.execute(
                "SELECT * FROM ReportSessions ORDER BY Id DESC LIMIT 1"
            ).fetchone()
    return dict(row) if row else None


# ── Rollback interface ───────────────────────────────────────────────────


def rollback_reports_for_session(
    session_id: str,
    db_path: Optional[str] = None,
) -> int:
    """Rollback report rows for a session.

    Deletes rows from ReportSessions, ReportMovies, and ReportTorrents
    (cascading delete handles ReportTorrents automatically if FK is set).

    Args:
        session_id: Session identifier
        db_path: Database path (defaults to REPORTS_DB_PATH)

    Returns:
        Number of rows deleted from ReportSessions
    """
    _ensure_imports()

    with _get_db(db_path or _REPORTS_DB_PATH) as conn:
        # Delete ReportMovies (ReportTorrents cascade via FK)
        conn.execute(
            "DELETE FROM ReportMovies WHERE SessionId = ?",
            (session_id,),
        )
        # Delete ReportSessions
        cur = conn.execute(
            "DELETE FROM ReportSessions WHERE Id = ?",
            (session_id,),
        )
        return cur.rowcount or 0


